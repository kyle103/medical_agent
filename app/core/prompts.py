    @classmethod
    def get_prompt(cls, prompt_name: str, **kwargs) -> str:
        """
        获取提示词，支持动态参数注入
        Args:
            prompt_name: 提示词名称
            **kwargs: 动态参数
        Returns:
            处理后的提示词
        """
        prompt = getattr(cls, prompt_name, None)
        if prompt is None:
            raise ValueError(f"Prompt {prompt_name} not found")
        
        # 注入动态参数
        if kwargs:
            return prompt.format(**kwargs)
        return prompt